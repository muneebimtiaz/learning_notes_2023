
    // while loop
    // Like an if statement, the body of a while loop is executed if the condition is true
    let i=0;
    let num=6;
    while(i<num){
        console.log("Come On!!");
        i+=1;
    }
    
    // do while loop
    let j=0;
    let num2=6;
    do{
        console.log("run one time ");
    }while(j<num2){
        console.log("hello world");
        j+=1;
    }

    // for loop
    // for loops are often used to do something a set number of times.
    for(let counter=0;counter<6;counter++){
        console.log("bye world!");
    }
    for(let counter=0;counter<6;counter=counter*2){
        console.log("bye world!");
    }
